use minigrep;

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn one_result() {
        let query = "duct";
        let contents = "\
Rust:
safe, fast, productive.
Pick three.";
        assert_eq!(
            vec!["safe, fast, productive."],
            minigrep::search(query, contents)
        );
    }

    #[test]
    fn case_sensitive() {
        let query = "duct";
        let contents = "\
Rust:
safe, fast, productive.
Pick three.
Duct tape.";

        assert_eq!(
            vec!["safe, fast, productive."],
            minigrep::search(query, contents)
        );
    }

    #[test]
    fn case_insensitive() {
        let query = "rUsT";
        let contents = "\
Rust:
safe, fast, productive.
Pick three.
Trust me.";

        assert_eq!(
            vec!["Rust:", "Trust me."],
            minigrep::search_case_insensitive(query, contents)
        );
    }

    // IGNORE_CASE=1 cargo run to poem.tx
    // $Env:IGNORE_CASE=1; cargo run to poem.txt

    /*
    Are you nobody, too?
    How dreary to be somebody!
    To tell your name the livelong day
    To an admiring bog!
    */

    // Remove-Item Env:IGNORE_CASE
    // cargo run to poem.txt

    /*
    Are you nobody, too?
    How dreary to be somebody!
    */
}
